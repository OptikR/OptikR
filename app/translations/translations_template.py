class Translator:
    """Simple translator for UI strings."""
    
    def __init__(self, language: str = "en"):
        self.language = language if language in ["en", "de", "fr", "it", "tr", "ja"] else "en"
    
    def set_language(self, language: str):
        if language in ["en", "de", "fr", "it", "tr", "ja"]:
            self.language = language
    
    def tr(self, key: str, *args) -> str:
        if key not in TRANSLATIONS:
            return key
        
        translation = TRANSLATIONS[key].get(self.language, TRANSLATIONS[key].get("en", key))
        
        if args:
            try:
                translation = translation.format(*args)
            except:
                pass
        
        return translation


# Global translator instance
_translator = Translator()


def set_language(language: str):
    """Set the global language."""
    _translator.set_language(language)


def tr(key: str, *args) -> str:
    """Translate a key using the global translator."""
    return _translator.tr(key, *args)
